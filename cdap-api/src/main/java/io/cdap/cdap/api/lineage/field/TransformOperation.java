package io.cdap.cdap.api.lineage.field;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;
import java.util.Objects;

/**
 * Represent the transform operation from collection of input fields to collection of output
 * fields.
 */
public class TransformOperation extends Operation {

  private final List<InputField> inputs;
  private final List<String> outputs;

  /**
   * Create the instance of a transform operation.
   *
   * @param name the name of the operation
   * @param description the description of the operation
   * @param inputs the list of input fields for operation
   * @param outputs the array of output fields generated by operation
   */
  public TransformOperation(String name, String description, List<InputField> inputs,
      String... outputs) {
    this(name, description, inputs, Arrays.asList(outputs));
  }

  /**
   * Create the instance of a transform operation.
   *
   * @param name the name of the operation
   * @param description the description of the operation
   * @param inputs the list of input fields for operation
   * @param outputs the list of output fields generated by operation
   */
  public TransformOperation(String name, String description, List<InputField> inputs,
      List<String> outputs) {
    super(name, OperationType.TRANSFORM, description);
    this.inputs = Collections.unmodifiableList(new ArrayList<>(inputs));
    this.outputs = Collections.unmodifiableList(new ArrayList<>(outputs));
  }

  /**
   * @return the list of input fields for this transform operation
   */
  public List<InputField> getInputs() {
    return inputs;
  }

  /**
   * @return the list of output fields generated by this transform operation
   */
  public List<String> getOutputs() {
    return outputs;
  }

  @Override
  public boolean equals(Object o) {
    if (this == o) {
      return true;
    }

    if (!super.equals(o)) {
      return false;
    }
    TransformOperation that = (TransformOperation) o;
    return Objects.equals(inputs, that.inputs)
        && Objects.equals(outputs, that.outputs);
  }

  @Override
  public int hashCode() {
    return Objects.hash(super.hashCode(), inputs, outputs);
  }
}
