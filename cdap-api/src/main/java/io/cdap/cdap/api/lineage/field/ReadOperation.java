package io.cdap.cdap.api.lineage.field;

import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collections;
import java.util.List;
import java.util.Objects;

/**
 * Represents a read operation from a data source into a collection of output fields.
 */
public class ReadOperation extends Operation {

  private final EndPoint source;
  private final List<String> outputs;

  /**
   * Create an instance of read operation.
   *
   * @param name the name of the operation
   * @param description the description associated with the operation
   * @param source the source for the operation
   * @param outputs the array of output fields to be generated
   */
  public ReadOperation(String name, String description, EndPoint source, String... outputs) {
    this(name, description, source, Arrays.asList(outputs));
  }

  /**
   * Create an instance of read operation.
   *
   * @param name the name of the operation
   * @param description the description associated with the operation
   * @param source the source for the operation
   * @param outputs the list of output fields to be generated
   */
  public ReadOperation(String name, String description, EndPoint source, List<String> outputs) {
    super(name, OperationType.READ, description);
    this.source = source;
    this.outputs = Collections.unmodifiableList(new ArrayList<>(outputs));
  }

  /**
   * @return the source associated with the read operation
   */
  public EndPoint getSource() {
    return source;
  }

  /**
   * Get the list of outputs generated by this read operation
   *
   * @return the list of outputs
   */
  public List<String> getOutputs() {
    return outputs;
  }

  @Override
  public boolean equals(Object o) {
    if (this == o) {
      return true;
    }

    if (!super.equals(o)) {
      return false;
    }
    ReadOperation that = (ReadOperation) o;
    return Objects.equals(source, that.source)
        && Objects.equals(outputs, that.outputs);
  }

  @Override
  public int hashCode() {
    return Objects.hash(super.hashCode(), source, outputs);
  }
}
